"""
Operadores de Associação

Nome = "Marcos"

Operador In --> x in y
Operador not in --> x not in y

"""

nome_1 = "pernambuco"

nome_2 = "per"

print(nome_2 in nome_1)
print("per" in nome_1)
print("nam" in nome_1)
print("ilha" in nome_1)
